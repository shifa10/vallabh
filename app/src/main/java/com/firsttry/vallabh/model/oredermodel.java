package com.firsttry.vallabh.model;

public class oredermodel {

    int orederimage;
    String solditem,price,oredernmber;


    public oredermodel() {
        this.orederimage = orederimage;
        this.solditem = solditem;
        this.price = price;
        this.oredernmber = oredernmber;
    }

    public int getOrederimage() {
        return orederimage;
    }

    public void setOrederimage(int orederimage) {
        this.orederimage = orederimage;
    }

    public String getSolditem() {
        return solditem;
    }

    public void setSolditem(String solditem) {
        this.solditem = solditem;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getOredernmber() {
        return oredernmber;
    }

    public void setOredernmber(String oredernmber) {
        this.oredernmber = oredernmber;
    }
}
